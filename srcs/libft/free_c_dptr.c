#include "libft.h"

void	free_c_dptr(char ***ptr)
{
	size_t	i;

	i = 0;
	while ((*ptr)[i])
	{
		safe_free((void **) &((*ptr)[i]));
		i++;
	}
	free(*ptr);
	*ptr = NULL;
}
